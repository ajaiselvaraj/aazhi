/**
 * Translate a stored English department name to the current UI language.
 * The `t` param is the translation function from LanguageContext.
 * Falls back to the raw English value if no key is matched.
 */
export function deptName(dept: string | undefined, t: (key: string) => string): string {
  if (!dept) return t('common.all_departments') || 'All Departments'
  const map: Record<string, string> = {
    'Electricity Department':  t('login.dept_electricity') || dept,
    'Water Supply Department': t('login.dept_water')       || dept,
    'Gas Distribution':        t('login.dept_gas')         || dept,
    'Municipal Services':      t('login.dept_municipal')   || dept,
  }
  return map[dept] ?? dept
}

